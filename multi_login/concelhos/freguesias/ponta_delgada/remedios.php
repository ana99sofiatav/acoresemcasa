    <p></p><h1>Factos</h1>Ponta Delgada é uma cidade portuguesa na ilha de São Miguel, na Região Autónoma dos Açores. Possui cerca de 46.102 (no perimetro urbano) e 20.113 habitantes nas três freguesias principalmente inseridas na parte central da cidade: São Pedro, São Sebastião, São José. É a capital administrativa do Arquipélago desde que os distritos foram extintos, por volta de 1976 (conjuntamente com Angra do Heroísmo (sede de diocese) e Horta, onde se sedia o Parlamento regional).
<h1></h1>
É sede de um município com 231,90 km² de área e 65.853 habitantes (2001), subdividido em 24 freguesias. O município é limitado a leste pelos municípios da Ribeira Grande e da Lagoa e tem costa no oceano Atlântico a norte, sul e oeste.
 
