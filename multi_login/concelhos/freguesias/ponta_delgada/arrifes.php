                        <h1>Curiosidades e História</h1>
                        <img style="display: block;  width:100%; max-height:380px;object-fit:contain;" src="../imgens/ponta_delgada/arrifes/pastagem.png" alt="Bretanha"/>
                        <p></p>
                        A freguesia dos Arrifes ocupa uma zona de terrenos que servem a agricultura e a pecuária. Quer dentro da freguesia, quer no seu redor existem pastagens para o gado bovino. Daí se dizer que é na área dos Arrifes que se encontra a maior “bacia leiteira” de toda a ilha.
                        <p></p>
                        Podemos dividir a sua população em dois grandes grupos: o que trabalha a terra e o que todos os dias se desloca à cidade para trabalhar no comércio, na indústria e serviços. É, de salientar, que a localidade em que a escola se insere, tem por maioria uma população que trabalha por conta de outrem. 
                        <p></p>
                        Nos últimos tempos, o desenvolvimento da indústria foi significativo, aparecendo iniciativas no campo oficinal (serralharias) e no campo industrial (lacticínios e construção civil). A nível cultural existem associações que têm por objectivo a promoção cultural e social da população. São de referir grupos como a Associação Cultural Juventude Arrifes, Escutismo, Lira Nª Sr.ª da Saúde, Casa do Povo e Grupo Desportivo “Águia Clube Desportivo”. 
                        <p></p>
                        No aspecto sociocultural merece destaque a filarmónica de Nª.Sª. da Saúde, que teve origem no ano de 1910, e as actividades relacionadas com a casa do povo dos Arrifes: o grupo folclórico, a escola de instrumentos de corda e o grupo de marchas e cantares populares. 
