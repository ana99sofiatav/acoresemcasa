                  <p></p>
                  <br> Os marinheiros puseram em prática todos os seus conhecimentos de navegação, mas a força do vento e das altas vagas era tanta que de nada lhes valeu arte e vigor. Com as velas rotas e o leme partido, o navio andava à deriva e a tripulação estava desorientada sem saber o que fazer, temendo que o frágil barco se despedaçasse contra os rochedos altos e escarpados da costa. <br><br>
                  <br>
